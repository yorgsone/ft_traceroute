#include "../../include/libft.h"

int	ft_atoi(const char *str)
{
	long	r;
	int		sign;

	while ((*str == 32) || (*str >= 9 && *str <= 13))
		str++;
	sign = (*str == 45) ? -1 : 1;
	(*str == 45 || *str == 43) ? str++ : str;
	r = 0;
	while (*str >= '0' && *str <= '9')
		r = r * 10 + *str++ - '0';
	return ((int)r * sign);
}
