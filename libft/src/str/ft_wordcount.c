#include "../../include/libft.h"

int	ft_wordcount(char *s, char c)
{
	int i;

	i = 0;
	while (*s && *(s + 1))
	{
		if (*s != c && *(s + 1) == c)
			i++;
		s++;
	}
	return (i);
}
